import React, { useEffect, useState } from 'react'
import { IoIosArrowRoundBack } from 'react-icons/io'
import { IoLocationSharp, IoSearchOutline } from 'react-icons/io5'
import { TbCurrentLocation } from 'react-icons/tb'
import { MapContainer, Marker, TileLayer, useMap } from 'react-leaflet'
import { useDispatch, useSelector } from 'react-redux'
import "leaflet/dist/leaflet.css"
import { setLocation, setNewAddress } from '../redux/mapSlice'
import { MdDeliveryDining } from "react-icons/md";
import axios from "axios"
import { FaMobileScreenButton } from 'react-icons/fa6'
import { FaCreditCard } from 'react-icons/fa'
import { useNavigate } from 'react-router-dom'
import { userServiceUrl } from '../App'
import { setAddOrders } from '../redux/userSlice'
function RecentMap({ location }) {
    const map = useMap();
    if (location?.lat && location?.lon) {
        map.setView([location.lat, location.lon], 16, { animate: true });
    }

    return null;
}
function CheckOut() {
    const {location, address}=useSelector(state=>state.map);
    const {cartItems, totalAmount}=useSelector(state=>state.user);
    const [addressInput, setAddressInput]=useState("");
    const [paymentMethod,setPaymentMethod]=useState("cod");
    const navigate=useNavigate();
    const api_key = import.meta.env.VITE_GEO_API_KEY;
    const deliveryFee=totalAmount>500 ? 0 :40;
    const amountWithDeliveryFee=totalAmount+deliveryFee;
    const dispatch=useDispatch();
    
    const onDragEnd = (e) => {
        const {lat, lng}=e.target.getLatLng();
        dispatch(setLocation({ lat, lon: lng }));
        getAddressByLatLong(lat,lng)
    };
    const getAddressByLatLong=async(latitude,longitude)=>{
        try {
            
            const result = await axios.get(
          `https://api.geoapify.com/v1/geocode/reverse?lat=${latitude}&lon=${longitude}&format=json&apiKey=${api_key}`
        );
        const addressObj = result?.data?.results?.[0];

        if (!addressObj) {
            console.log("No address found");
            return;
        }
        dispatch(setNewAddress(addressObj.address_line2))
        } catch (error) {
         console.log(error);   
        }
    }
    const getCurrentLocation=()=>{
         navigator.geolocation.getCurrentPosition(async (position) => {
        try {
            const latitude = position.coords.latitude;
            const longitude = position.coords.longitude;
            dispatch(setLocation({lat:latitude,lon:longitude}))
            getAddressByLatLong(latitude,longitude)
        }
        catch(error){
            console.log(error); 
        }
    })
}
    const getLatLngByAddress = async () => {
    try {
        const api_key = import.meta.env.VITE_GEO_API_KEY;

        const result = await axios.get(
            `https://api.geoapify.com/v1/geocode/search?text=${encodeURIComponent(addressInput)}&apiKey=${api_key}`
        );

        const firstResult = result?.data?.features?.[0];

        if (!firstResult) {
            alert("No location found for this address");
            return;
        }

        const { lat, lon } = firstResult.properties;

        dispatch(setLocation({ lat, lon }));

    } catch (error) {
        console.log(error);
    }
};
    const handlePlaceOrder=async()=>{
        try {
            const result=await axios.post(`${userServiceUrl}/api/v1/orders/place-order`,{
                paymentMethod,
                deliveryAddress:{
                    text:addressInput,
                    latitude:location.lat,
                    longitude:location.lon
                },
                totalAmount,
                cartItems
            },{withCredentials:true});
            console.log(result.data)
            dispatch(setAddOrders(result.data))
            navigate("/order-placed");
        } catch (error) {
            console.log(error);
        }
    }
    
    useEffect(()=>{
        setAddressInput(address);
    },[address])
  return (
    <div className='min-h-screen bg-[#fff9f6] flex items-center justify-center p-6'>
        <div className='absolute top-5 left-5 z-10' onClick={()=>navigate("/")}>
            <IoIosArrowRoundBack size={35} className='text-[#ff4d2d]'/>
        </div>
        <div className='w-full max-w-[900px] bg-white rounded-2xl shadow-xl p-6 space-y-6'>
            <h1 className='text-2xl font-bold text-start'> Your Checkout</h1>
            <section>
                <h2 className='text-lg font-semibold mb-2 flex items-center gap-2 text-gray-800'>
                    <IoLocationSharp className='text-[#ff4d2d]'/> Delivery Location
                </h2>
                <div className='flex gap-2 mb-3'>
                    <input type="text" value={addressInput} onChange={(e)=>{setAddressInput(e.target.value)}} className='flex-1 border border-gray-300 rounded-lg p-2 text-sm focus:outline-none focus:ring-2 focus:ring-[#ff4d2d]' placeholder='Enter your delivery Address'/>
                    <button className='bg-[#ff4d2d] hover:bg-[#e64526] text-white px-3 py-2 rouned-lg items-center
                    justify-center' onClick={getLatLngByAddress}>
                        <IoSearchOutline size={17}/>
                    </button>
                    <button className='bg-blue-500 hover:bg-blue-600 text-white px-3 py-2 rouned-lg items-center
                    justify-center' onClick={getCurrentLocation}>
                        <TbCurrentLocation size={17}/>
                    </button>
                </div>
                <div className='rounded-xl border overflow-hidden'>
                    <div className='h-64 w-full flex items-center justify-center'>

                        <MapContainer
                            className="w-full h-full"
                            center={[location?.lat, location?.lon]}
                            zoom={16}
                        >
                            <TileLayer
                                attribution='&copy; OpenStreetMap contributors'
                                url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png"
                            />

                            <RecentMap location={location} />

                            <Marker
                                position={[location?.lat, location?.lon]}
                                draggable
                                eventHandlers={{ dragend: onDragEnd }}
                            />
                        </MapContainer>

                    </div>
                </div>
            </section>
            <section>
                <h2 className='text-lg font-semibold mb-3 text-gray-800'>  Payment Method </h2>
                <div className='grid grid-cols-1 sm:grid-cols-2 gap-4'>
                    <div className={`flex items-center gap-3 rounded-xl border p-4 text-left transition ${paymentMethod==="cod" ? "border-[#ff4d2d] bg-orange-50 shadow " :"border-gray-200 hover:border-gray-300"}`} onClick={()=>setPaymentMethod("cod")}>
                        <span className='inline-flex h-10 w-10 items-center justify-center rounded-full bg-green-100'>
                            <MdDeliveryDining className='text-green-600 text-xl'/>
                        </span>
                        <div>
                            <p className='font-medium text-gray-800'>Cash On Delivery</p>
                            <p className='text-xs text-gray-800'>Pay When your food Delivered</p>
                        </div>
                    </div>
                     <div className={`flex items-center gap-3 rounded-xl border p-4 text-left transition ${paymentMethod==="online" ? "border-[#ff4d2d] bg-orange-50 shadow " :"border-gray-200 hover:border-gray-300"}`} onClick={()=>setPaymentMethod("online")}>
                        <span className='inline-flex h-10 w-10 items-center justify-center rounded-full bg-purple-100'>
                            <FaMobileScreenButton className='text-purple-700 text-lg'/>
                        </span>
                        <span className='inline-flex h-10 w-10 items-center justify-center rounded-full bg-blue-100'>
                            <FaCreditCard className='text-blue-700 text-lg'/>
                        </span>
                        <div>
                            <p className='font-medium text-gray-800'>UPI/ Credit/Debit Card</p>
                            <p className='text-xs text-gray-800'>Pay Online</p>
                        </div>
                    </div>
                </div>
            </section>
            <section>
                <h2 className='text-lg font-semibold mb-3 text-gray-800'>Order Summary</h2>
                <div className='rounded-xl border bg-gray-50 p-4 space-y-2'>
                    {cartItems.map((item,index)=>(
                        <div key={index} className='flex justify-between text-sm text-gray-700'>
                            <span>{item.name} x {item.quantity}</span>
                            <span>₹ {item.price} x {item.quantity}</span>
                        </div>
                    ))}
                    <hr className='border-gray-200 my-2'/>
                    <div className='flex justify-between font-medium text-gray-800'>
                        <span>SubTotal</span>
                        <span>₹ {totalAmount}</span>
                    </div>
                    <div className='flex justify-between text-gray-700'>
                        <span>Delivery Fee</span>
                        <span>{deliveryFee == 0 ? "free" : `₹ ${deliveryFee}`}</span>
                    </div>
                    <div className='flex justify-between text-lg font-bold text-[#ff4d2d] pt-2'>
                        <span>Total </span>
                        <span>₹ {amountWithDeliveryFee}</span>
                    </div>
                </div>
            </section>
            <button onClick={handlePlaceOrder} className='w-full cursor-pointer bg-[#ff4d2d] hover:bg[#e64526] text-white py-3 rounded-xl font-semibold'> {paymentMethod=="cod"?"Place Order":"Pay & Place Order"}</button>
        </div>
    </div>
  )
}

export default CheckOut
